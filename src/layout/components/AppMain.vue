<template>
  <section class="app-main">
    <transition name="fade-transform" mode="out-in">
      <keep-alive :include="cachedViews">
        <router-view :key="key" />
      </keep-alive>
    </transition>
  </section>
</template>

<script>
export default {
  name: 'AppMain',
  computed: {
    cachedViews() {
      return this.$store.state.tagsView.cachedViews
    },
    key() {
      console.log("router-view path ===> "+this.$route.path);
      console.log("router-view  query ====> " + JSON.stringify(this.$route.query));
      
      // 解决章节管理 已打开时重新刷新的问题
      if("/course/chapters" == this.$route.path){
        return this.$route.path + "/" + this.$route.query.isVip + "/" + this.$route.query.id
      }
      return this.$route.path
    }
  }
}
</script>

